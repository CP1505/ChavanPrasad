package MethodsType;

public class Calculator {
	// BLC
	
		// 4. ==> 1. addition 2. Substraction 3. Multiplication 4. Division.
		
		
		// 1. addition.
		
		public int getsum(int num1,int num2) {
			return num1+num2;
		}
		
		// 2. Substraction
		
		public int getsubstraction(int num1,int num2) {
				return num1-num2;
		}
		
		
		// 3. Multiplication
		
		public int getmultiplication(int num1,int num2) {
				return num1*num2;
		}
		
		// 4. Division
		
		public int getdivision(int num1,int num2) {
			return num1/num2;
		}
		
		public int getsum(int num1,int num2,int num3) {
			return num1+num2+num3;
		}
		
		public double getsum(int num1, double num2) {
			return num1+num2;
		}
		
	}

