package MethodPractice;
//Methods Call From Diffrent Class.
public class Sample {
//Business Logic Class
	//Static method 
	
	public static void Bike1() {
		System.out.println("Bike1 Static Method From Sample Class");
	}
	public static void Bike2() {
		System.out.println("Bike2 Static Method From Sample Class");
	}
//Non static Method    
	public void Car1() {
		System.out.println("Car1 Non Static Method From Sample Class");
	}
    public void Car2() {
	System.out.println("Car2 Non Static Method From Sample Class");
    }

}
