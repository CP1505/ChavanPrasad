package MethodsTypePractice;

public class HpCalsi {
//BLC
	//Addition
	public int getsum(int num1,int num2) {
		return num1+num2;
	}

//Substraction
	
	public int getsub(int num1,int num2) {
		return num1-num2;
		
	}
//multiplication
	public int getmulti(int num1,int num2) {
		return num1*num2;
	}
//Divide
	public int getdiv(int num1,int num2) {
		return num1/num2;
	}











}
