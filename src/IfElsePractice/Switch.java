package IfElsePractice;

public class Switch {
public static void main(String[] args)
{
	int color=4;
	
switch(color) {
	case 1: System.out.println("It is Yellow color");break;
	case 2:System.out.println("It is a black color");break;
	case 3: System.out.println("It is a white color");break;
	case 4:System.out.println("It is a red color");break;
	case 5: System.out.println("It is a orange color");break;
	default:System.out.println("It is not a color");}
}
}

